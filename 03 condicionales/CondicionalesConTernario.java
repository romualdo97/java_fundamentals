public class CondicionalesConTernario {

	public static void main(String[] args) {
		
		int i; //Declarión de la variable
		i = 4; //Inicialización de la variable
		String result; // declaracion de la variable
		result = i > 0 ? "es verdadera" : "no es verdadera"; // usando operador ternario para evaluar condicion
		System.out.print("La condición " + result);

	}

}